package baekjoon.implementation;

/**
 * 백준 알고리즘
 * Implementation (구현)
 * 고양이
 * https://www.acmicpc.net/problem/10171
 */
public class Cat {

    public static void main(String[] args) {
        /**
         * \    /\
         *  )  ( ')
         * (  /  )
         *  \(__)|
         * 고양이 그리기
         * escape 출력시 주의 하라는 문제인듯
         * 인텔리 j는 자동으로 인식해서 넣어주니 편하다
         */
        System.out.println("\\    /\\");
        System.out.println(" )  ( ')");
        System.out.println("(  /  )");
        System.out.println(" \\(__)|");
    }
}
